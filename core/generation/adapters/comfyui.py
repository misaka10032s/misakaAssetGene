DEFAULT_NEGATIVE_PROMPT = "low quality, blurry, deformed, extra limbs, bad anatomy"


def adapter_name() -> str:
    return "comfyui"


def execute(context: AdapterContext) -> AdapterExecutionResult:
    base_url = _resolve_base_url(context.health_check)
    checkpoint_name = _resolve_checkpoint_name(context.worker_path)
    prompt_id = uuid.uuid4().hex
    filename_prefix = f"misaka-{context.job.project_id}-{context.job.id[:8]}"

    with httpx.Client(timeout=60.0) as client:
        recipe = _resolve_recipe(context)
        if context.report_progress:
            context.report_progress(10, f"Preparing {recipe.value} workflow")
        workflow = _build_workflow(
            recipe=recipe,
            client=client,
            base_url=base_url,
            checkpoint_name=checkpoint_name,
            positive_prompt=context.job.prompt,
            negative_prompt=DEFAULT_NEGATIVE_PROMPT,
            filename_prefix=filename_prefix,
            seed=_stable_seed(prompt_id),
            source_asset_path=context.source_asset_path,
            mask_asset_path=context.mask_asset_path,
        )
        if context.report_progress:
            context.report_progress(35, "Submitting workflow to ComfyUI")
        response = client.post(
            f"{base_url}/prompt",
            json={
                "prompt": workflow,
                "client_id": f"misaka-{context.job.project_id}",
                "prompt_id": prompt_id,
            },
        )
        response.raise_for_status()

        history = _wait_for_history(client, base_url, prompt_id, context)
        images = _download_output_images(client, base_url, history)

    if not images:
        raise RuntimeError("ComfyUI finished without returning any generated images.")

    artifacts: list[GeneratedArtifact] = []
    for index, image in enumerate(images, start=1):
        suffix = Path(image["filename"]).suffix or ".png"
        artifacts.append(
            GeneratedArtifact(
                modality=Modality.IMAGE,
                asset_type="image",
                title=context.job.title if index == 1 else f"{context.job.title} #{index}",
                filename=f"{filename_prefix}-{index}{suffix}",
                description=f"Generated by ComfyUI with checkpoint {checkpoint_name}.",
                content=image["content"],
            )
        )
    return AdapterExecutionResult(artifacts=artifacts, metadata={"prompt_id": prompt_id, "checkpoint": checkpoint_name})


def _resolve_recipe(context: AdapterContext) -> GenerationRecipe:
    recipe = context.job.recipe or GenerationRecipe.AUTO
    if recipe is GenerationRecipe.AUTO:
        if context.source_asset_path and context.mask_asset_path:
            return GenerationRecipe.INPAINT
        if context.source_asset_path:
            return GenerationRecipe.IMG2IMG
        return GenerationRecipe.TXT2IMG
    return recipe


def _resolve_base_url(health_check: str | None) -> str:
    if not health_check:
        raise RuntimeError("ComfyUI health check URL is missing.")
    return health_check.removesuffix("/system_stats").rstrip("/")


def _resolve_checkpoint_name(worker_path: Path) -> str:
    checkpoint_dir = worker_path / "models" / "checkpoints"
    checkpoint_files = sorted(
        item.name
        for item in checkpoint_dir.glob("*")
        if item.is_file() and item.name != "put_checkpoints_here"
    )
    if not checkpoint_files:
        raise RuntimeError("ComfyUI has no installed checkpoint.")
    return checkpoint_files[0]


