def _stable_seed(prompt_id: str) -> int:
    return int(prompt_id[:8], 16)


def _upload_input_asset(client: httpx.Client, base_url: str, asset_path: Path) -> str:
    with asset_path.open("rb") as file_handle:
        response = client.post(
            f"{base_url}/upload/image",
            files={"image": (asset_path.name, file_handle, "application/octet-stream")},
            data={"type": "input", "overwrite": "true"},
        )
    response.raise_for_status()
    payload = response.json()
    return str(payload.get("name") or asset_path.name)


def _wait_for_history(client: httpx.Client, base_url: str, prompt_id: str, context: AdapterContext) -> dict:
    for _ in range(120):
        response = client.get(f"{base_url}/history/{prompt_id}")
        response.raise_for_status()
        payload = response.json()
        history = payload.get(prompt_id)
        if history and history.get("outputs"):
            if context.report_progress:
                context.report_progress(85, "Downloading generated images")
            return history
        time.sleep(1)
    raise RuntimeError("ComfyUI did not finish within the expected timeout.")


def _download_output_images(client: httpx.Client, base_url: str, history: dict) -> list[dict[str, bytes | str]]:
    output_images: list[dict[str, bytes | str]] = []
    outputs = history.get("outputs") or {}
    for node_output in outputs.values():
        for image in node_output.get("images", []):
            query = urlencode(
                {
                    "filename": str(image.get("filename") or ""),
                    "subfolder": str(image.get("subfolder") or ""),
                    "type": str(image.get("type") or "output"),
                }
            )
            response = client.get(f"{base_url}/view?{query}")
            response.raise_for_status()
            output_images.append({"filename": str(image.get("filename") or "output.png"), "content": response.content})
    return output_images
